const CHILD_UI_TRANSLATIONS: Record<string, ChildUiTranslationTable> = {
  lg: LUGANDA_CHILD_UI,
  nyn: RUNYANKOLE_CHILD_UI,
};

const interpolate = (
  value: string,
  params?: ChildUiTranslationParams,
): string => {
  if (!params) return value;

  return value.replace(/\{(\w+)\}/g, (match, name: string) => {
    const replacement = params[name];
    return replacement === undefined ? match : String(replacement);
  });
};

export const translateChildUi = (
  key: ChildUiTranslationKey,
  languageCode?: string | null,
  enabled = false,
  params?: ChildUiTranslationParams,
): string => {
  const english = CHILD_UI_ENGLISH[key];
  if (!enabled) return interpolate(english, params);

  const normalizedLanguageCode = normalizeLearningLanguageCode(languageCode);
  const translated = normalizedLanguageCode
    ? CHILD_UI_TRANSLATIONS[normalizedLanguageCode]?.[key]
    : undefined;

  return interpolate(translated ?? english, params);
};

