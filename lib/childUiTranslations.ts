export const translateChildUiAchievement = (
  achievement: ChildUiAchievementSource,
  languageCode?: string | null,
  enabled = false,
): ChildUiAchievementText => {
  if (!enabled) {
    return {
      name: achievement.name,
      description: achievement.description,
    };
  }

  const normalizedLanguageCode = normalizeLearningLanguageCode(languageCode);
  const translated = normalizedLanguageCode
    ? ACHIEVEMENT_TRANSLATIONS[normalizedLanguageCode]?.[achievement.id]
    : undefined;

  return {
    name: translated?.name ?? achievement.name,
    description: translated?.description ?? achievement.description,
  };
};
